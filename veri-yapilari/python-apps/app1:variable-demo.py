#Uygulama 1: Bir öğrencinin aşağıdaki bilgileri için gerekli değişkenleri oluşturunuz.

#Öğrenci adı
ogrenciAdi = 'Alperen'
print(ogrenciAdi)

#Öğrenci soyadı
ogrenciSoyad = 'Tavas'
print(ogrenciSoyad)

#Öğrenci ad + soyad
ogrenciAdSoyad = ogrenciAdi + ' ' + ogrenciSoyad
print(ogrenciAdSoyad)

#Öğrenci numarası
ogrenciNo = '123456'
print(ogrenciNo)

#Öğrenci cinsiyeti
ogrenciCinsiyet = True #Erkek
print(ogrenciCinsiyet)
ogrenciCinsiyet = False #Kadın
print(ogrenciCinsiyet)

#Öğrenci tc kimlik
ogrenciTc = '45985369412'
print(ogrenciTc)

#Öğrenci doğum yılı
ogrecinDogum = 1996
print(ogrecinDogum)

#Öğrenci adres bilgisi
ogrenciAdres = 'İstanbul'
print(ogrenciAdres)

#Öğrenci yaşı
ogrenciYas = '26'
print(ogrenciYas)

